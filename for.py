#Bucle for range(inicio,fin,paso)


for numero in range(2,21,2): #inicia dos termina antes del 21 , suma dos
    print(numero) 
    
for numero in range(1,6,1):# inicia en uno termina antes del 1 suma 1 
    print(numero)
print('----------------------------------')  
#resta
for numero in range(10,0,-1): # 10,9,8,7,.....
    print(numero)
    
#Pasos mas grandes
print('---------------------------------')
for i in range(0,100,25):
    print(i)